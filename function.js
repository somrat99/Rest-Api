/**
 * skill View in Dom
 * @param {*} id 
 * @returns 
 */

function skillPreview(id) {

    if (id == "1") {
        return ` Laravel Developer `;
    }else if ( id == "2" ) {
        return ` React Developer `;
    }else if ( id == "3" ) {
        return ` Django Developer `;
    }else if ( id == "4" ) {
        return ` Python Developer `;
    }else if ( id == "5" ) {
        return ` WordPress Developer `;
    }
    
}